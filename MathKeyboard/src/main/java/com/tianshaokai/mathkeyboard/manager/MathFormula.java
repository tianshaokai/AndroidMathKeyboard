package com.tianshaokai.mathkeyboard.manager;

/**
 * 数学公式列举
 */
public enum MathFormula {
    UnKnow,         //未知
    Fraction,       //分数

    Sqrt,           //根号
    Sqrt2,          //根号开平方

    Power,          //幂次方
    Power2,         //二次方
    PowerX2,         //X的二次方
    Power10,        //10的幂次方

    Sum,            //求和

    Sine,           //正弦
    Cosine,         //余弦
    Tangent,        //tan

    PIR2,        //πr²

    Equation,       //方程式
    Equation_3,       //三项方程式
    AbsoluteValue,  //绝对值
    SubScript,      //下标
    SubScriptX1,      //下标x1
    SubScriptX2,      //下标x1

    Angle,          //三角

    Circ,           //度数
    Minute,         //分
    Second,         //秒


    Arc             //弧

}